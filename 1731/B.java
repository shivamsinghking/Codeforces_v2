    public static long pwr(long x, long y, long p) { 
        // Initialize result 
        long res = 1;      
         
        // Update x if it is more   
        // than or equal to p 
        x = x % p;  
  
        if (x == 0) return 0; // In case x is divisible by p;    
  
        while (y > 0) { 
            // If y is odd, multiply x 
            // with result 
            if((y & 1)==1) 
                res = (res * x) % p; 
      
            // y must be even now 
            // y = y / 2 
            y = y >> 1;  
            x = (x * x) % p;  
        } 
        return res; 
    }
    
    static long sum(long n){
    	return ((n*(n + 1)%MOD))*(pwr(2, MOD - 2, MOD))%MOD;
    }
    
    static long square_sum(long n){
    	return (((n*(n + 1)%MOD)*(2*n + 1))%MOD)*(pwr(6, MOD - 2, MOD))%MOD;
    }
    public static void solve() {
      long n = sc.nextLong();
      
      long ans = 0L;
      ans += square_sum(n)%MOD;
      ans += square_sum(n - 1)%MOD;
      ans += sum(n - 1)%MOD;
      ans *= 2022L;
      ans = ans%MOD;
      
      out.println(ans);
    }

    public static long gcd(long a, long b) {
        while (b != 0) {
            long rem = a % b;
            a = b;
            b = rem;
        }
        return a;
    }

